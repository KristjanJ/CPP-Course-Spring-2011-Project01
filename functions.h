#ifndef FUNCTIONS_H_INCLUDED
#define FUNCTIONS_H_INCLUDED

/*!
\file functions.h
\brief This header file  contains some function prototypes.
*/

/*!
\brief Calculates the minimum value
\param a Value 1
\param b Value 2
\return The minimum of a and b
\retval long
*/
long minimum(long a, long b);

/*!
\brief Calculates the maximum value
\param a Value 1
\param b Value 2
\return The maximum of a and b
\retval long
*/
long maximum(long a, long b);

/*!
\brief Calculates the sum
\param a Value 1
\param b Value 2
\return The sum of a and b
\retval long
*/
long sum(long a, long b);

/*!
\brief Calculates the product
\param a Value 1
\param b Value 2
\return The product of a and b
\retval long
*/
long product(long a, long b);

/*!
\brief Calculates the greatest common divisor
\param a Value 1
\param b Value 2
\return The greatest common divisor of a and b
\retval long
*/
long gcd(long a, long b);

#endif // FUNCTIONS_H_INCLUDED
